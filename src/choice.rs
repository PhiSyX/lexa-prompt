use std::{error, fmt, str};

use crate::{Choice, Result};

// -------- //
// Fonction //
// -------- //

/// Invite l'utilisateur à entrer une valeur parmi des choix.
pub fn choice<Output>(message: impl fmt::Display) -> Result<Output>
where
	Output: str::FromStr,
	Output::Err: Into<Box<dyn error::Error>>,
	Output: Choice,
{
	let msg = format!("{message}:");

	let choice = inquire::Select::new(&msg, Output::choices().to_vec())
		.prompt()
		.map(|choice| Output::from_str(&choice).map_err(|e| e.into()))??;

	Ok(choice)
}
